/* The identifier to hold data that does not vary is called 'Constant' and to declare a constant, 
'const' keyword is used, followed by an identifier. The value is initialized during the declaration itself and cannot be altered later. */

const pi=3.14;

console.log("The value of pi is "+pi);

/*As a best practice, the const declaration can be used for string type identifiers or simple number, 
functions or classes which does not need to be changed or value*/